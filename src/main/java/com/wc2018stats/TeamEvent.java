package com.wc2018stats;

public class TeamEvent {
    public int id;
    public String type_of_event;
    public String player;
    public String time;


    public int getId() {
        return id;
    }

    public String getType_of_event() {
        return type_of_event;
    }

    public String getPlayer() {
        return player;
    }

    public String getTime() {
        return time;
    }
}
